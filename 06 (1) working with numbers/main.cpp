#include <iostream>
using namespace std;

int main(){
    cout << 10 << 2 << endl; // Output: 40
    cout << 10 + 2 << endl; // Output: 12
    cout << 10 - 2 << endl; // Output: 8
    cout << 10 * 2 << endl; // Output: 20
    cout << 10 / 2 << endl; // Output: 5
    cout << 10 % 2 << endl; // Output: 0
    cout << 10 + 2 * 3 << endl; // Output: 16
    cout << (10 + 2) * 3 << endl; // Output: 36


    int num = 10;
    cout << num++ << endl; // Output: 10
    cout << ++num << endl; // Output: 12
    cout << num-- << endl; // Output: 12
    cout << --num << endl; // Output: 10
    cout << num << endl; // Output: 10
    cout << num + 1 << endl; // Output: 11
    cout << num - 1 << endl; // Output: 9


    return 0;
}